package LearnJava;

public class IncrementDecrement {
    public static void main(String[] args){
        char x = 'A';
        System.out.println(x);
        System.out.println(++x);
        int a = 4;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
    }
}
